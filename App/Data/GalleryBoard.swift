import SwiftUI
import Defaults

struct GalleryBoard: Codable, Identifiable, Hashable {
    var id: UUID
    var title: String
    var symbol: String
    var color: Color
    var sounds: [GallerySound]

    init(id: UUID = .init(), title: String, symbol: String, color: Color, sounds: [GallerySound]) {
        self.id = id
        self.title = title
        self.symbol = symbol
        self.color = color
        self.sounds = sounds
    }

    public func set<Value>(_ keyPath: WritableKeyPath<Self, Value>, to value: Value) -> Self {
        var object = self
        object[keyPath: keyPath] = value
        return object
    }

    @discardableResult
    func save() -> Board {
        let sounds = self.sounds.map({ $0.save() })
        let board = Board(
            title: self.title,
            symbol: self.symbol,
            color: self.color,
            sounds: sounds.map(\.id),
            galleryID: self.id
        )
        Defaults[.boards].append(board)
        return board
    }

